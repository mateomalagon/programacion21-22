	public static void main (String[] args) {
    
/*Muestra los números del 320 al 160, contando de 20 en 20 hacia atrás utilizando
un bucle do-while.*/

    
  System.out.println("Del 320 al 160 de 20 en 20: ");
    int n=320;
  
  do {
      System.out.println(n);
    n-=20;
  } while (n >= 160);
 }   
